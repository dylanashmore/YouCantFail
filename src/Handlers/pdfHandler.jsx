// Exports list of PDFs for each selected topic for topicButton.jsx randomly
export function getRandomPdfFromTopics(topics) {
  const allPdfs = topics.flatMap(topic => TOPIC_PDFS[topic] || []);
  if (allPdfs.length === 0) return null;
  const idx = Math.floor(Math.random() * allPdfs.length);
  return allPdfs[idx];
}

export function getSolutionPdfForQuestion(questionPdfUrl) {
  if (!questionPdfUrl) return null;

  // Ensure the path starts with /Questions/
  if (!questionPdfUrl.startsWith('/Questions/')) return null;

  const parts = questionPdfUrl.split('/');
  if (parts.length < 4) return null;

  const topic = parts[2];
  const filename = parts.slice(3).join('/'); // in case of subfolders

  // Build the solution filename
  const solutionFilename = filename.replace(/\.pdf$/, '-sol.pdf');
  const solutionPath = `/Solutions/${topic}/${solutionFilename}`;
  const topicKey = `Solutions/${topic}`;

  // Check if this solution exists in ANSWER_PDFS
  if (
    ANSWER_PDFS[topicKey] &&
    ANSWER_PDFS[topicKey].includes(solutionPath)
  ) {
    return solutionPath;
  }
  return null;
}

